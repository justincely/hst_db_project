CREATE TABLE exposure (
  rootname CHAR(9) PRIMARY KEY,
  obsname CHAR(9) REFERENCES observation (rootname),
  instrument CHAR(4) NOT NULL,
  detector CHAR(4) NOT NULL,
  mjd DOUBLE PRECISION CHECK (mjd > 0),
  exptime DOUBLE PRECISION CHECK (exptime > 0),
  obsmode VARCHAR(80) CHECK (obsmode = 'ACCUM' OR obsmode = 'TIME-TAG'),
  cenwave INT CHECK (cenwave > 0),
  opt_elem CHAR(6),
  FOREIGN KEY (detector, instrument) REFERENCES detector (name, inst)
);

