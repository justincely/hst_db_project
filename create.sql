CREATE FUNCTION log_exptime() RETURNS trigger AS $log_exptime$
BEGIN
  UPDATE exptime_count SET count = (count + NEW.exptime) WHERE instrument = NEW.instrument;
  RETURN NEW;
END;
$log_exptime$ LANGUAGE plpgsql;
  
  
